"""   Area,circumference of sq,rectangle,circle
"""

from math import pi

def area_of_square(side):
    """
    Area of Square
    :param side: side of square
    :return:Area
    """
    return side*side

def area_of_circle(rad):
    """
    Area of circle
    :param rad: rad of circle
    :return:Area
    """
    return pi*rad*rad
def area_of_rectangle(length,breadth):
    """
    Area of Square
    :param length,breadth: side of rectangle
    :return:Area
    """
    return length*breadth